

# 시퀀스 with => slice [] [:]

print(b[0:2])  # [4, 5]
print(b[0:])  # [4, 5, 6, 7, 8, 9, 10]
print(b[0:-1])  # [4, 5, 6, 7, 8, 9]
print(b[0:-1:2])  # 2씩 증가하면서 가져오기 [4, 6, 8]

print(b[::2])  # [4, 6, 8, 10] 짝수번 인덱스만 가져오기.

print(b[::-1])  # 뒤 집기 [10, 9, 8, 7, 6, 5, 4]

print(b[1::] + b[0:1:])  # 로테이션 [5, 6, 7, 8, 9, 10, 4]
