import sys


sys.setrecursionlimit(10**6)
input = sys.stdin.readline

ans = 0
for i in range(8):
    row = list(input().rstrip())
    for (j, r) in enumerate(row):
        if (i+j) % 2 == 0 and r == 'F':
            ans += 1
print(ans)

